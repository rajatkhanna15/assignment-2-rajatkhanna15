
package problem4.node;


public class Node {
    /*
    Some variables to be used
     */
    int data;
    Node next;

    /*
    getter and setter methods for variables.
     */


    public int getData() {
        return data;
    }



    public void setData(int data) {
        this.data = data;
    }

    public Node getNext() {
        return next;
    }

    public void setNext(Node next) {
        this.next = next;
    }
}
